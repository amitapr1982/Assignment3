import math

number = float(input("Enter a number: "))

# Calculate the Square root of the number
square_root= math.sqrt(number)

# Calculate Natural logarithm (log base e) of the number
natural_num = math.log(number)

#Calculate Sine of the number (in radians)
sine_num = math.sin(number)

print(f"Square root: {square_root}")
print(f"Natural number: {natural_num}")
print(f"Sine number: {sine_num}")


